"""
AegisHeuristics - Benchmark Telemetry Parser
This script ingests the JSON telemetry generated by Google Benchmark, processes
the hardware-level latency metrics using Pandas, and outputs publication-ready 
vector graphics (SVG) optimized for integration into scientific reports.
"""

import json
import os
import sys
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

# Configuration constraints
TELEMETRY_PATH = "../build/benchmark_telemetry.json"
OUTPUT_GRAPH = "benchmark_results.svg"

def load_telemetry(file_path):
    """
    Loads and parses the JSON output from Google Benchmark.
    """
    if not os.path.exists(file_path):
        print(f"[ERROR] Telemetry file not found at: {file_path}")
        print("Ensure you have executed the benchmark binary with the JSON flag.")
        sys.exit(1)
        
    with open(file_path, 'r') as f:
        data = json.load(f)
        
    return data['benchmarks']

def process_data(benchmarks):
    """
    Transforms the raw JSON arrays into a structured Pandas DataFrame.
    Filters out unnecessary Google Benchmark metadata.
    """
    records = []
    for run in benchmarks:
        # Extract the core function name, removing arguments if any
        name = run['name'].split('/')[0]
        
        # We focus on CPU time rather than real time to isolate the algorithm's
        # mathematical throughput from OS scheduler interruptions.
        cpu_time_ns = run.get('cpu_time', 0.0)
        iterations = run.get('iterations', 0)
        
        records.append({
            "Benchmark": name,
            "CPU Time (ns)": cpu_time_ns,
            "Iterations": iterations
        })
        
    return pd.DataFrame(records)

def generate_vector_graphics(df, output_path):
    """
    Renders a high-contrast, publication-ready bar chart using Seaborn.
    Exports strictly to SVG to maintain infinite resolution for PDF compilation.
    """
    # Set a clean, academic aesthetic
    sns.set_theme(style="whitegrid")
    
    # Initialize the matplotlib figure
    plt.figure(figsize=(10, 6))
    
    # Create a bar plot visualizing CPU time per benchmark
    ax = sns.barplot(
        x="CPU Time (ns)", 
        y="Benchmark", 
        data=df, 
        color="#2c3e50", # Dark slate gray for professional contrast
        edgecolor="black"
    )
    
    # Title and labels mapping
    plt.title("AegisHeuristics - Micro-Benchmark CPU Latency", fontsize=16, fontweight='bold', pad=15)
    plt.xlabel("CPU Execution Time (Nanoseconds)", fontsize=12, fontweight='bold')
    plt.ylabel("Algorithm Routine", fontsize=12, fontweight='bold')
    
    # Annotate exact nanosecond values inside the bars for precise data reading
    for p in ax.patches:
        width = p.get_width()
        plt.text(
            width - (width * 0.05), # Offset slightly to the left of the bar's end
            p.get_y() + p.get_height() / 2, 
            f"{width:,.2f} ns", 
            ha="right", 
            va="center", 
            color="white", 
            fontweight='bold',
            fontsize=10
        )
    
    # Strip unnecessary borders to maintain a clean Data-to-Ink ratio
    sns.despine(left=True, bottom=True)
    
    # Export the vector graphic
    plt.tight_layout()
    plt.savefig(output_path, format="svg", bbox_inches="tight")
    print(f"[SUCCESS] High-resolution vector graphic saved to: {output_path}")

def main():
    print(" AegisHeuristics - Telemetry Analysis Pipeline")
    
    print(f"-> Reading raw hardware telemetry from: {TELEMETRY_PATH}")
    raw_data = load_telemetry(TELEMETRY_PATH)
    
    print("-> Structuring dataset...")
    df = process_data(raw_data)
    
    print("-> Rendering vector graphics...")
    generate_vector_graphics(df, OUTPUT_GRAPH)
    
    print(" Pipeline execution completed.")

if __name__ == "__main__":
    main()